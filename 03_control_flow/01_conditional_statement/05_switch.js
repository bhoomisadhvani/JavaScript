// the switch case is use for multiple cases in one condition.

let day = "Monday";

switch (day) {
  case "Monday":
    console.log("Start of the  week - 05_switch.js:7");
    break;
  case "Friday":
    console.log("Almost weekend! - 05_switch.js:10");
    break;
  case "Sunday":
    console.log("Rest day - 05_switch.js:13");
    break;
  default:
    console.log("Just another day - 05_switch.js:16");
    break;
}
